// fetch-dwarf-elements.mjs — pull real osculating elements for the IAU dwarf
// planets from JPL Horizons (heliocentric, ecliptic J2000), so each rides its
// real orbit in the system view: Pluto's Neptune-crossing tilt, Eris's wild
// 44°-inclined path out past 90 AU, Haumea and Makemake in the scattered disc.
//
//   node tools/fetch-dwarf-elements.mjs
//
// They orbit slowly (Pluto 248 yr, Eris 558 yr), so a single epoch of osculating
// elements + plain Kepler propagation is accurate across the current era — no
// two-step period refinement needed (unlike the fast inner moons).  a in metres,
// angles in degrees.  Writes src/dwarf-elements.js.

import { writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const EPOCH = '2026-01-01';

// [name, Horizons id] — a ';' suffix marks a small-body designation; Pluto is a
// major body (999).  All queried heliocentric (CENTER 500@10 = the Sun).
const DWARFS = [
  ['Ceres', '1;'],
  ['Pluto', '999'],
  ['Haumea', '136108;'],
  ['Makemake', '136472;'],
  ['Eris', '136199;'],
];

const num = (s, key) => {
  const m = s.match(new RegExp(`(?:^|\\s)${key}\\s*=\\s*(-?[\\d.]+E?[+-]?\\d*)`, 'm'));
  return m ? parseFloat(m[1]) : null;
};

async function fetchEls(id) {
  const p = new URLSearchParams({
    format: 'text', COMMAND: `'${id}'`, OBJ_DATA: 'NO', MAKE_EPHEM: 'YES',
    EPHEM_TYPE: 'ELEMENTS', CENTER: "'500@10'",
    REF_PLANE: 'ECLIPTIC', REF_SYSTEM: 'J2000', TLIST: `'${EPOCH}'`, OUT_UNITS: 'KM-S',
  });
  const txt = await (await fetch(`https://ssd.jpl.nasa.gov/api/horizons.api?${p}`)).text();
  const block = txt.match(/\$\$SOE([\s\S]*?)\$\$EOE/);
  if (!block) return null;
  const b = block[1];
  const a = num(b, 'A'), e = num(b, 'EC');
  if (a == null || e == null || e >= 1 || a <= 0) return null;
  const epochJd = parseFloat(b.trim().split(/\s+/)[0]);
  return {
    a: a * 1000, e, i: num(b, 'IN'), node: num(b, 'OM'), peri: num(b, 'W'),
    M0: num(b, 'MA'), periodDays: num(b, 'PR') / 86400, epochJd,
  };
}

const round = (n, d) => Number(n.toFixed(d));
const out = {};
let epochJd = null;
for (const [name, id] of DWARFS) {
  process.stdout.write(`${name.padEnd(10)} `);
  try {
    const el = await fetchEls(id);
    if (!el) { console.log('— no usable orbit (skipped)'); continue; }
    epochJd = el.epochJd;
    out[name] = {
      a: round(el.a, 0), e: round(el.e, 6), i: round(el.i, 4), node: round(el.node, 4),
      peri: round(el.peri, 4), M0: round(el.M0, 4), periodDays: round(el.periodDays, 3),
    };
    console.log(`a=${(el.a / 1.495978707e11).toFixed(2)}AU  e=${el.e.toFixed(3)}  i=${el.i.toFixed(1)}°  P=${(el.periodDays / 365.25).toFixed(1)}yr`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await new Promise((r) => setTimeout(r, 350));
}

const lines = Object.entries(out).map(([n, e]) =>
  `  ${JSON.stringify(n)}: { a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, periodDays: ${e.periodDays} }`,
).join(',\n');
const file = `// dwarf-elements.js — GENERATED by tools/fetch-dwarf-elements.mjs.  Real
// osculating elements (JPL Horizons, epoch ${EPOCH}), heliocentric ecliptic
// J2000.  a in metres, angles in degrees.  The dwarf planets orbit slowly, so a
// single epoch + plain Kepler propagation holds across the current era.

export const DWARF_EPOCH_JD = ${epochJd};

export const DWARF_ELEMENTS = {
${lines},
};
`;
const dest = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'dwarf-elements.js');
await writeFile(dest, file);
console.log(`\nwrote ${Object.keys(out).length} dwarf planets → ${dest}`);
