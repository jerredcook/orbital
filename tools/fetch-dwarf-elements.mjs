// fetch-dwarf-elements.mjs — pull real osculating elements for the IAU dwarf
// planets from JPL Horizons (heliocentric, ecliptic J2000), so each rides its
// real orbit in the system view: Pluto's Neptune-crossing tilt, Eris's wild
// 44°-inclined path out past 90 AU, Haumea and Makemake in the scattered disc.
//
//   node tools/fetch-dwarf-elements.mjs
//
// They orbit slowly (Pluto 248 yr, Eris 558 yr), so a single epoch of osculating
// elements + plain Kepler propagation is accurate across the current era — no
// two-step period refinement needed (unlike the fast inner moons).  a in metres,
// angles in degrees.  Writes src/dwarf-elements.js.

import {
  AU_M, sleep, round, horizonsElements, soeBlock, parseElements, srcPath, writeElementsModule,
} from './horizons.mjs';

const EPOCH = '2026-01-01';

// [name, Horizons id] — a ';' suffix marks a small-body designation; Pluto is a
// major body (999).  All queried heliocentric (CENTER 500@10 = the Sun).
const DWARFS = [
  ['Ceres', '1;'], ['Pluto', '999'], ['Haumea', '136108;'], ['Makemake', '136472;'], ['Eris', '136199;'],
];

const out = {};
let epochJd = null;
for (const [name, id] of DWARFS) {
  process.stdout.write(`${name.padEnd(10)} `);
  try {
    const block = soeBlock(await horizonsElements(id, { epoch: EPOCH }));
    const el = block && parseElements(block);
    if (!el || el.e >= 1 || el.a <= 0) { console.log('— no usable orbit (skipped)'); continue; }
    epochJd = el.epochJd;
    out[name] = {
      a: round(el.a, 0), e: round(el.e, 6), i: round(el.i, 4), node: round(el.node, 4),
      peri: round(el.peri, 4), M0: round(el.M0, 4), periodDays: round(el.periodDays, 3),
    };
    console.log(`a=${(el.a / AU_M).toFixed(2)}AU  e=${el.e.toFixed(3)}  i=${el.i.toFixed(1)}°  P=${(el.periodDays / 365.25).toFixed(1)}yr`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await sleep(350);
}

const dest = srcPath(import.meta.url, 'dwarf-elements.js');
const n = await writeElementsModule({
  dest, constName: 'DWARF_ELEMENTS', epochConst: 'DWARF_EPOCH_JD', epochJd, entries: out,
  fmt: (e) => `{ a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, periodDays: ${e.periodDays} }`,
  header: `// dwarf-elements.js — GENERATED by tools/fetch-dwarf-elements.mjs.  Real
// osculating elements (JPL Horizons, epoch ${EPOCH}), heliocentric ecliptic
// J2000.  a in metres, angles in degrees.  The dwarf planets orbit slowly, so a
// single epoch + plain Kepler propagation holds across the current era.`,
});
console.log(`\nwrote ${n} dwarf planets → ${dest}`);
