// fetch-probe-elements.mjs — pull real osculating elements for the planetary
// probes from JPL Horizons (ecliptic-J2000, planet-centred), so each orbiter
// shows its real shape + orientation (Juno's eccentric polar ellipse, etc.)
// instead of a tidy illustrative circle.  Spacecraft trajectories only exist in
// Horizons while the craft is active, so each is queried at a mid-mission epoch.
//
//   node tools/fetch-probe-elements.mjs
//
// Phase (M0) isn't observable at this scale and these are osculating, so we keep
// e / i / node / peri (shape + orientation) and the period; the renderer anchors
// periapsis to the compressed radius.  Probes Horizons doesn't track are skipped
// (they keep their illustrative orbit).  Writes src/probe-elements.js.

import { writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const CENTER = { Mercury: '199', Venus: '299', Mars: '499', Jupiter: '599', Saturn: '699' };

// [name, Horizons spacecraft id, planet, mid-mission epoch]
const PROBES = [
  ['Juno', '-61', 'Jupiter', '2024-01-01'],
  ['Galileo', '-77', 'Jupiter', '2000-01-01'],
  ['Cassini', '-82', 'Saturn', '2012-01-01'],
  ['MESSENGER', '-236', 'Mercury', '2013-01-01'],
  ['Magellan', '-25', 'Venus', '1992-01-01'],
  ['Venus Express', '-248', 'Venus', '2010-01-01'],
  ['Akatsuki', '-5', 'Venus', '2018-01-01'],
  ['MRO', '-74', 'Mars', '2024-01-01'],
  ['MAVEN', '-202', 'Mars', '2024-01-01'],
  ['Mars Odyssey', '-53', 'Mars', '2020-01-01'],
  ['Mars Express', '-41', 'Mars', '2020-01-01'],
  ['Mars Global Surveyor', '-94', 'Mars', '2002-01-01'],
  ['ExoMars TGO', '-143', 'Mars', '2024-01-01'],
  ['Mangalyaan', '-3', 'Mars', '2018-01-01'],
  ['Tianwen-1', '-55', 'Mars', '2024-01-01'],
  ['Hope', '-62', 'Mars', '2024-01-01'],
  ['Mariner 9', '-9', 'Mars', '1972-01-01'],
  ['Viking 1 Orbiter', '-27', 'Mars', '1977-06-01'],
  ['Viking 2 Orbiter', '-30', 'Mars', '1977-06-01'],
  ['Pioneer Venus', '-24', 'Venus', '1985-01-01'],
];

const num = (s, key) => {
  const m = s.match(new RegExp(`(?:^|\\s)${key}\\s*=\\s*(-?[\\d.]+E?[+-]?\\d*)`, 'm'));
  return m ? parseFloat(m[1]) : null;
};

async function fetchEls(id, planet, date) {
  const p = new URLSearchParams({
    format: 'text', COMMAND: `'${id}'`, OBJ_DATA: 'NO', MAKE_EPHEM: 'YES',
    EPHEM_TYPE: 'ELEMENTS', CENTER: `'500@${CENTER[planet]}'`,
    REF_PLANE: 'ECLIPTIC', REF_SYSTEM: 'J2000', TLIST: `'${date}'`, OUT_UNITS: 'KM-S',
  });
  const txt = await (await fetch(`https://ssd.jpl.nasa.gov/api/horizons.api?${p}`)).text();
  const block = txt.match(/\$\$SOE([\s\S]*?)\$\$EOE/);
  if (!block) return null;
  const b = block[1];
  const a = num(b, 'A'), e = num(b, 'EC');
  if (a == null || e == null || e >= 1 || a <= 0) return null;   // need a bound ellipse
  return { a: a * 1000, e, i: num(b, 'IN'), node: num(b, 'OM'), peri: num(b, 'W'), M0: num(b, 'MA'), periodDays: num(b, 'PR') / 86400 };
}

const round = (n, d) => Number(n.toFixed(d));
const out = {};
for (const [name, id, planet, date] of PROBES) {
  process.stdout.write(`${name.padEnd(22)} `);
  try {
    const el = await fetchEls(id, planet, date);
    if (!el) { console.log('— no usable orbit (skipped)'); continue; }
    out[name] = { a: round(el.a, 0), e: round(el.e, 5), i: round(el.i, 3), node: round(el.node, 3), peri: round(el.peri, 3), M0: round(el.M0, 3), periodDays: round(el.periodDays, 5) };
    console.log(`e=${el.e.toFixed(3)}  i=${el.i.toFixed(1)}°  P=${el.periodDays.toFixed(3)}d`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await new Promise((r) => setTimeout(r, 350));
}

const lines = Object.entries(out).map(([n, e]) =>
  `  ${JSON.stringify(n)}: { a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, periodDays: ${e.periodDays} }`,
).join(',\n');
const file = `// probe-elements.js — GENERATED by tools/fetch-probe-elements.mjs.  Real
// osculating elements (JPL Horizons, mid-mission), ecliptic J2000, planet-centred.
// Used for each probe's orbit shape + orientation; a in metres, angles in degrees.

export const PROBE_ELEMENTS = {
${lines},
};
`;
const dest = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'probe-elements.js');
await writeFile(dest, file);
console.log(`\nwrote ${Object.keys(out).length} probes → ${dest}`);
