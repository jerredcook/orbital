// fetch-probe-elements.mjs — pull real osculating elements for the planetary
// probes from JPL Horizons (ecliptic-J2000, planet-centred), so each orbiter
// shows its real shape + orientation (Juno's eccentric polar ellipse, etc.)
// instead of a tidy illustrative circle.  Spacecraft trajectories only exist in
// Horizons while the craft is active, so each is queried at a mid-mission epoch.
//
//   node tools/fetch-probe-elements.mjs
//
// Phase (M0) isn't observable at this scale and these are osculating, so we keep
// e / i / node / peri (shape + orientation) and the period; the renderer anchors
// periapsis to the compressed radius.  Probes Horizons doesn't track are skipped
// (they keep their illustrative orbit).  Writes src/probe-elements.js.

import {
  sleep, round, horizonsElements, soeBlock, parseElements, srcPath, writeElementsModule,
} from './horizons.mjs';

const CENTER = { Mercury: '199', Venus: '299', Mars: '499', Jupiter: '599', Saturn: '699' };

// [name, Horizons spacecraft id, planet, mid-mission epoch]
const PROBES = [
  ['Juno', '-61', 'Jupiter', '2024-01-01'], ['Galileo', '-77', 'Jupiter', '2000-01-01'],
  ['Cassini', '-82', 'Saturn', '2012-01-01'], ['MESSENGER', '-236', 'Mercury', '2013-01-01'],
  ['Magellan', '-25', 'Venus', '1992-01-01'], ['Venus Express', '-248', 'Venus', '2010-01-01'],
  ['Akatsuki', '-5', 'Venus', '2018-01-01'], ['MRO', '-74', 'Mars', '2024-01-01'],
  ['MAVEN', '-202', 'Mars', '2024-01-01'], ['Mars Odyssey', '-53', 'Mars', '2020-01-01'],
  ['Mars Express', '-41', 'Mars', '2020-01-01'], ['Mars Global Surveyor', '-94', 'Mars', '2002-01-01'],
  ['ExoMars TGO', '-143', 'Mars', '2024-01-01'], ['Mangalyaan', '-3', 'Mars', '2018-01-01'],
  ['Tianwen-1', '-55', 'Mars', '2024-01-01'], ['Hope', '-62', 'Mars', '2024-01-01'],
  ['Mariner 9', '-9', 'Mars', '1972-01-01'], ['Viking 1 Orbiter', '-27', 'Mars', '1977-06-01'],
  ['Viking 2 Orbiter', '-30', 'Mars', '1977-06-01'], ['Pioneer Venus', '-24', 'Venus', '1985-01-01'],
];

const out = {};
for (const [name, id, planet, date] of PROBES) {
  process.stdout.write(`${name.padEnd(22)} `);
  try {
    const block = soeBlock(await horizonsElements(id, { center: CENTER[planet], epoch: date }));
    const el = block && parseElements(block);
    if (!el || el.e >= 1 || el.a <= 0) { console.log('— no usable orbit (skipped)'); continue; }   // need a bound ellipse
    out[name] = {
      a: round(el.a, 0), e: round(el.e, 5), i: round(el.i, 3), node: round(el.node, 3),
      peri: round(el.peri, 3), M0: round(el.M0, 3), periodDays: round(el.periodDays, 5),
    };
    console.log(`e=${el.e.toFixed(3)}  i=${el.i.toFixed(1)}°  P=${el.periodDays.toFixed(3)}d`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await sleep(350);
}

const dest = srcPath(import.meta.url, 'probe-elements.js');
const n = await writeElementsModule({
  dest, constName: 'PROBE_ELEMENTS', entries: out,
  fmt: (e) => `{ a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, periodDays: ${e.periodDays} }`,
  header: `// probe-elements.js — GENERATED by tools/fetch-probe-elements.mjs.  Real
// osculating elements (JPL Horizons, mid-mission), ecliptic J2000, planet-centred.
// Used for each probe's orbit shape + orientation; a in metres, angles in degrees.`,
});
console.log(`\nwrote ${n} probes → ${dest}`);
