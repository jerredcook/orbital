// fetch-interstellar-elements.mjs — pull real osculating elements for the known
// interstellar objects from JPL Horizons (heliocentric, ecliptic J2000).  These
// are on UNBOUND hyperbolic orbits (e>1): they fell in from interstellar space,
// whipped once around the Sun, and are leaving forever.
//
//   node tools/fetch-interstellar-elements.mjs
//
// Hyperbolic orbits have no period, so we store the mean motion n (deg/day)
// instead and propagate M = M0 + n·Δt, solving the hyperbolic Kepler equation
// M = e·sinh(H) − H.  Horizons returns N in deg/SECOND under KM-S units, so we
// scale by 86400.  a is |semi-major axis| (Horizons A is negative for e>1).
// Writes src/interstellar-elements.js.

import { writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const EPOCH = '2026-01-01';

// [display name, Horizons designation]
const OBJECTS = [
  ['Oumuamua', '1I'],
  ['Borisov', '2I'],
  ['3I/ATLAS', '3I'],
];

const num = (s, key) => {
  const m = s.match(new RegExp(`(?:^|\\s)${key}\\s*=\\s*(-?[\\d.]+E?[+-]?\\d*)`));
  return m ? parseFloat(m[1]) : null;
};

async function fetchEls(id) {
  const p = new URLSearchParams({
    format: 'text', COMMAND: `'${id}'`, OBJ_DATA: 'NO', MAKE_EPHEM: 'YES',
    EPHEM_TYPE: 'ELEMENTS', CENTER: "'500@10'",
    REF_PLANE: 'ECLIPTIC', REF_SYSTEM: 'J2000', TLIST: `'${EPOCH}'`, OUT_UNITS: 'KM-S',
  });
  const txt = await (await fetch(`https://ssd.jpl.nasa.gov/api/horizons.api?${p}`)).text();
  const block = txt.match(/\$\$SOE([\s\S]*?)\$\$EOE/);
  if (!block) return null;
  const b = block[1];
  const a = num(b, 'A'), e = num(b, 'EC');
  if (a == null || e == null || e <= 1) return null;   // must be genuinely unbound
  const epochJd = parseFloat(b.trim().split(/\s+/)[0]);
  return {
    a: Math.abs(a) * 1000, e, i: num(b, 'IN'), node: num(b, 'OM'), peri: num(b, 'W'),
    M0: num(b, 'MA'), n: num(b, 'N') * 86400, epochJd,   // N: deg/s → deg/day
  };
}

const round = (n, d) => Number(n.toFixed(d));
const AU = 1.495978707e11;
const out = {};
let epochJd = null;
for (const [name, id] of OBJECTS) {
  process.stdout.write(`${name.padEnd(10)} `);
  try {
    const el = await fetchEls(id);
    if (!el) { console.log('— no unbound orbit (skipped)'); continue; }
    epochJd = el.epochJd;
    out[name] = {
      a: round(el.a, 0), e: round(el.e, 6), i: round(el.i, 4), node: round(el.node, 4),
      peri: round(el.peri, 4), M0: round(el.M0, 4), n: round(el.n, 8),
    };
    const q = (el.a * (el.e - 1)) / AU;
    console.log(`e=${el.e.toFixed(2)}  i=${el.i.toFixed(1)}°  q=${q.toFixed(2)}AU  n=${el.n.toFixed(3)}°/day`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await new Promise((r) => setTimeout(r, 400));
}

const lines = Object.entries(out).map(([n, e]) =>
  `  ${JSON.stringify(n)}: { a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, n: ${e.n} }`,
).join(',\n');
const file = `// interstellar-elements.js — GENERATED by tools/fetch-interstellar-elements.mjs.
// Real osculating elements (JPL Horizons, epoch ${EPOCH}), heliocentric ecliptic
// J2000, for the known interstellar objects.  UNBOUND hyperbolic orbits (e>1):
// a is |semi-major axis| in metres, angles in degrees, n is the mean motion in
// deg/day (M = M0 + n·Δt, then solve M = e·sinh(H) − H).

export const INTERSTELLAR_EPOCH_JD = ${epochJd};

export const INTERSTELLAR_ELEMENTS = {
${lines},
};
`;
const dest = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'interstellar-elements.js');
await writeFile(dest, file);
console.log(`\nwrote ${Object.keys(out).length} interstellar objects → ${dest}`);
