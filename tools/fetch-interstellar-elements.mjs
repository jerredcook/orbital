// fetch-interstellar-elements.mjs — pull real osculating elements for the known
// interstellar objects from JPL Horizons (heliocentric, ecliptic J2000).  These
// are on UNBOUND hyperbolic orbits (e>1): they fell in from interstellar space,
// whipped once around the Sun, and are leaving forever.
//
//   node tools/fetch-interstellar-elements.mjs
//
// Hyperbolic orbits have no period, so we store the mean motion n (deg/day)
// instead and propagate M = M0 + n·Δt, solving the hyperbolic Kepler equation
// M = e·sinh(H) − H.  Horizons returns N in deg/SECOND under KM-S units (parsed
// to deg/day by horizons.mjs).  a is |semi-major axis| (Horizons A is negative
// for e>1).  Writes src/interstellar-elements.js.

import {
  AU_M, sleep, round, horizonsElements, soeBlock, parseElements, srcPath, writeElementsModule,
} from './horizons.mjs';

const EPOCH = '2026-01-01';

// [display name, Horizons designation]
const OBJECTS = [['Oumuamua', '1I'], ['Borisov', '2I'], ['3I/ATLAS', '3I']];

const out = {};
let epochJd = null;
for (const [name, id] of OBJECTS) {
  process.stdout.write(`${name.padEnd(10)} `);
  try {
    const block = soeBlock(await horizonsElements(id, { epoch: EPOCH }));
    const el = block && parseElements(block);
    if (!el || el.e <= 1) { console.log('— no unbound orbit (skipped)'); continue; }   // must be genuinely unbound
    epochJd = el.epochJd;
    out[name] = {
      a: round(Math.abs(el.a), 0), e: round(el.e, 6), i: round(el.i, 4), node: round(el.node, 4),
      peri: round(el.peri, 4), M0: round(el.M0, 4), n: round(el.n, 8),
    };
    const q = (Math.abs(el.a) * (el.e - 1)) / AU_M;
    console.log(`e=${el.e.toFixed(2)}  i=${el.i.toFixed(1)}°  q=${q.toFixed(2)}AU  n=${el.n.toFixed(3)}°/day`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await sleep(400);
}

const dest = srcPath(import.meta.url, 'interstellar-elements.js');
const n = await writeElementsModule({
  dest, constName: 'INTERSTELLAR_ELEMENTS', epochConst: 'INTERSTELLAR_EPOCH_JD', epochJd, entries: out,
  fmt: (e) => `{ a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, n: ${e.n} }`,
  header: `// interstellar-elements.js — GENERATED by tools/fetch-interstellar-elements.mjs.
// Real osculating elements (JPL Horizons, epoch ${EPOCH}), heliocentric ecliptic
// J2000, for the known interstellar objects.  UNBOUND hyperbolic orbits (e>1):
// a is |semi-major axis| in metres, angles in degrees, n is the mean motion in
// deg/day (M = M0 + n·Δt, then solve M = e·sinh(H) − H).`,
});
console.log(`\nwrote ${n} interstellar objects → ${dest}`);
