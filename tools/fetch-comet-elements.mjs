// fetch-comet-elements.mjs — pull real osculating elements for a few famous
// comets from JPL Horizons (heliocentric, ecliptic J2000), so each rides its
// real orbit in the system view: Halley's retrograde 76-yr plunge, Encke's tidy
// short loop, Hale-Bopp's near-vertical giant ellipse, NEOWISE's long dive.
//
//   node tools/fetch-comet-elements.mjs
//
// Periodic comets (1P, 2P) resolve to a list of per-apparition records; we pick
// the most recent and re-query it.  Long-period comets resolve directly.  All
// osculating elements are propagated to the epoch below, so M0 places each comet
// where it really is now (mostly far out — the drama is the orbit).  a in metres,
// angles in degrees.  Writes src/comet-elements.js.

import { writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const EPOCH = '2026-01-01';
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// [name, Horizons designation]
const COMETS = [
  ['Halley', '1P'],
  ['Encke', '2P'],
  ['Hale-Bopp', 'C/1995 O1'],
  ['NEOWISE', 'C/2020 F3'],
];

async function horizons(command) {
  const p = new URLSearchParams({
    format: 'text', COMMAND: `'${command}'`, OBJ_DATA: 'NO', MAKE_EPHEM: 'YES',
    EPHEM_TYPE: 'ELEMENTS', CENTER: "'500@10'",
    REF_PLANE: 'ECLIPTIC', REF_SYSTEM: 'J2000', TLIST: `'${EPOCH}'`, OUT_UNITS: 'KM-S',
  });
  return (await (await fetch(`https://ssd.jpl.nasa.gov/api/horizons.api?${p}`)).text());
}

function parseEls(txt) {
  const block = txt.match(/\$\$SOE([\s\S]*?)\$\$EOE/);
  if (!block) return null;
  const s = block[1];
  const num = (k) => { const m = s.match(new RegExp(`(?:^|\\s)${k}\\s*=\\s*(-?[\\d.]+E?[+-]?\\d*)`)); return m ? parseFloat(m[1]) : null; };
  const a = num('A'), e = num('EC');
  if (a == null || e == null || e >= 1 || a <= 0) return null;
  const epochJd = parseFloat(s.trim().split(/\s+/)[0]);
  return { a: a * 1000, e, i: num('IN'), node: num('OM'), peri: num('W'), M0: num('MA'), periodDays: num('PR') / 86400, epochJd };
}

// From a disambiguation list, pick the record with the most recent epoch-year.
function pickLatestRecord(txt) {
  const rows = [...txt.matchAll(/^\s*(9\d{7})\s+(-?\d+)\s+/gm)].map((m) => ({ rec: m[1], yr: parseInt(m[2], 10) }));
  if (!rows.length) return null;
  rows.sort((a, b) => b.yr - a.yr);
  return rows[0].rec;
}

const round = (n, d) => Number(n.toFixed(d));
const AU = 1.495978707e11;
const out = {};
let epochJd = null;
for (const [name, desig] of COMETS) {
  process.stdout.write(`${name.padEnd(11)} `);
  try {
    let txt = await horizons(desig);
    let el = parseEls(txt);
    if (!el) {                          // periodic comet → resolve the latest apparition record
      const rec = pickLatestRecord(txt);
      if (rec) { await sleep(400); txt = await horizons(rec); el = parseEls(txt); }
    }
    if (!el) { console.log('— no usable orbit (skipped)'); continue; }
    epochJd = el.epochJd;
    out[name] = {
      a: round(el.a, 0), e: round(el.e, 6), i: round(el.i, 4), node: round(el.node, 4),
      peri: round(el.peri, 4), M0: round(el.M0, 4), periodDays: round(el.periodDays, 2),
    };
    const q = (el.a * (1 - el.e)) / AU, Q = (el.a * (1 + el.e)) / AU;
    console.log(`e=${el.e.toFixed(3)}  i=${el.i.toFixed(1)}°  q=${q.toFixed(2)}AU  Q=${Q.toFixed(0)}AU  P=${(el.periodDays / 365.25).toFixed(0)}yr`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await sleep(400);
}

const lines = Object.entries(out).map(([n, e]) =>
  `  ${JSON.stringify(n)}: { a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, periodDays: ${e.periodDays} }`,
).join(',\n');
const file = `// comet-elements.js — GENERATED by tools/fetch-comet-elements.mjs.  Real
// osculating elements (JPL Horizons, epoch ${EPOCH}), heliocentric ecliptic
// J2000.  a in metres, angles in degrees.  M0 places each comet where it really
// is now; the value on display is the dramatic orbit itself.

export const COMET_EPOCH_JD = ${epochJd};

export const COMET_ELEMENTS = {
${lines},
};
`;
const dest = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'comet-elements.js');
await writeFile(dest, file);
console.log(`\nwrote ${Object.keys(out).length} comets → ${dest}`);
