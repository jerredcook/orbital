// fetch-comet-elements.mjs — pull real osculating elements for a few famous
// comets from JPL Horizons (heliocentric, ecliptic J2000), so each rides its
// real orbit in the system view: Halley's retrograde 76-yr plunge, Encke's tidy
// short loop, Hale-Bopp's near-vertical giant ellipse, NEOWISE's long dive.
//
//   node tools/fetch-comet-elements.mjs
//
// Periodic comets (1P, 2P) resolve to a list of per-apparition records; we pick
// the most recent and re-query it.  Long-period comets resolve directly.  All
// osculating elements are propagated to the epoch below, so M0 places each comet
// where it really is now (mostly far out — the drama is the orbit).  a in metres,
// angles in degrees.  Writes src/comet-elements.js.

import {
  AU_M, sleep, round, horizonsElements, soeBlock, parseElements, pickLatestRecord,
  srcPath, writeElementsModule,
} from './horizons.mjs';

const EPOCH = '2026-01-01';

// [name, Horizons designation]
const COMETS = [
  ['Halley', '1P'],
  ['Encke', '2P'],
  ['Hale-Bopp', 'C/1995 O1'],
  ['NEOWISE', 'C/2020 F3'],
];

// Bound comet only (reject any hyperbolic/parabolic solution).
const boundEls = (txt) => {
  const el = parseElements(soeBlock(txt) ?? '');
  return el && el.e < 1 && el.a > 0 ? el : null;
};

const out = {};
let epochJd = null;
for (const [name, desig] of COMETS) {
  process.stdout.write(`${name.padEnd(11)} `);
  try {
    let txt = await horizonsElements(desig, { epoch: EPOCH });
    let el = boundEls(txt);
    if (!el) {                          // periodic comet → resolve the latest apparition record
      const rec = pickLatestRecord(txt);
      if (rec) { await sleep(400); txt = await horizonsElements(rec, { epoch: EPOCH }); el = boundEls(txt); }
    }
    if (!el) { console.log('— no usable orbit (skipped)'); continue; }
    epochJd = el.epochJd;
    out[name] = {
      a: round(el.a, 0), e: round(el.e, 6), i: round(el.i, 4), node: round(el.node, 4),
      peri: round(el.peri, 4), M0: round(el.M0, 4), periodDays: round(el.periodDays, 2),
    };
    const q = (el.a * (1 - el.e)) / AU_M, Q = (el.a * (1 + el.e)) / AU_M;
    console.log(`e=${el.e.toFixed(3)}  i=${el.i.toFixed(1)}°  q=${q.toFixed(2)}AU  Q=${Q.toFixed(0)}AU  P=${(el.periodDays / 365.25).toFixed(0)}yr`);
  } catch (err) { console.log(`FAILED ${err.message}`); }
  await sleep(400);
}

const dest = srcPath(import.meta.url, 'comet-elements.js');
const n = await writeElementsModule({
  dest, constName: 'COMET_ELEMENTS', epochConst: 'COMET_EPOCH_JD', epochJd, entries: out,
  fmt: (e) => `{ a: ${e.a}, e: ${e.e}, i: ${e.i}, node: ${e.node}, peri: ${e.peri}, M0: ${e.M0}, periodDays: ${e.periodDays} }`,
  header: `// comet-elements.js — GENERATED by tools/fetch-comet-elements.mjs.  Real
// osculating elements (JPL Horizons, epoch ${EPOCH}), heliocentric ecliptic
// J2000.  a in metres, angles in degrees.  M0 places each comet where it really
// is now; the value on display is the dramatic orbit itself.`,
});
console.log(`\nwrote ${n} comets → ${dest}`);
